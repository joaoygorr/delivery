CREATE TABLE public.driver (
	available bool NULL,
	id_driver int8 GENERATED BY DEFAULT AS IDENTITY( INCREMENT BY 1 MINVALUE 1 MAXVALUE 9223372036854775807 START 1 CACHE 1 NO CYCLE) NOT NULL,
	"name" varchar(255) NULL,
	phone_number varchar(255) NULL,
	vehicle varchar(255) NULL,
	CONSTRAINT driver_pkey PRIMARY KEY (id_driver)
);

CREATE TABLE public.delivery (
	estimated_delivery_time timestamp(6) NULL,
	id_delivery int8 GENERATED BY DEFAULT AS IDENTITY( INCREMENT BY 1 MINVALUE 1 MAXVALUE 9223372036854775807 START 1 CACHE 1 NO CYCLE) NOT NULL,
	id_driver int8 NULL,
	destination_address varchar(255) NULL,
	status varchar(255) NULL,
	pedidoId varchar(255),
	CONSTRAINT delivery_pkey PRIMARY KEY (id_delivery),
	CONSTRAINT delivery_driver FOREIGN KEY (id_driver) REFERENCES public.driver(id_driver)
);

CREATE TABLE public."location" (
	latitude float8 NOT NULL,
	longitude float8 NOT NULL,
	id_delivery int8 NULL,
	id_location int8 GENERATED BY DEFAULT AS IDENTITY( INCREMENT BY 1 MINVALUE 1 MAXVALUE 9223372036854775807 START 1 CACHE 1 NO CYCLE) NOT NULL,
	"timestamp" timestamp(6) NULL,
	CONSTRAINT location_pkey PRIMARY KEY (id_location),
	CONSTRAINT location_delivery FOREIGN KEY (id_delivery) REFERENCES public.delivery(id_delivery)
);

